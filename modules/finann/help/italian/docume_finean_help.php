<p>Men&ugrave; <strong>Fine anno</strong>. Qui si accede alle funzioni
relative alle operazioni che si svolgono comunemente alla fine dell'anno
di gestione. In particolare, da qui &egrave; possibile ottenere un
bilancio e produrre la chiusura e riapertura dei conti in
contabilit&agrave; generale.</p>

<ul>
<li>

    <p><strong>Chiusura-apertura conti</strong></p>

    <p>Da qui &egrave; possibile generare automaticamente le scritture
    di chiusura e riapertura, nella contabilit&agrave; generale. Questa
    operazione va fatta solo dopo che sono stati fatti gli assestamenti.
    Il bilancio che si ottiene con la voce <strong>Bilancio CEE</strong>
    può essere provato anche prima di avere generato le scritture di
    chiusura; d'altro canto, se in fase di stampa del bilancio ci si
    accorge di avere dimenticato qualche scrittura di assestamento o dei
    giroconti, ma la chiusura è già stata fatta, occorre provvedere
    manualmente alla cancellazione di tutte queste scritture
    automatiche, le quali poi possono essere rigenerate nuovamente.</p>

</li>
<li>

    <p><strong>Bilancio</strong></p>

    <p>Da qui &egrave; si ottiene la stampa di un bilancio di verifica.</p>

    <p>Esempio: <a title="guarda il video"
    href="http://www.youtube.com/watch?v=YpGhNKCNCJA">stampa del
    bilancio di verifica</a>.</p>

</li>
<li>

    <p><strong>Bilancio CEE</strong></p>

    <p>Da qui si ottiene la stampa del bilancio secondo lo schema
    stabilito dal codice civile. Questa stampa pu&ograve; essere
    effettuata soltanto se nel piano dei conti sono state fatte
    correttamente le associazioni alle voci di bilancio; inoltre, tutti
    i conti che per loro natura non devono confluire in bilancio
    direttamente (vedi ad esempio i conti &laquo;IVA n/credito&raquo;,
    &laquo;IVA n/debito&raquo;, &laquo;Istituti previdenziali&raquo;, e i
    conti bancari), alla fine degli assestamenti devono essere stati
    girati a conti più adatti alla rappresentazione in bilancio.</p>

    <p>Esempio: <a title="guarda il video"
    href="http://www.youtube.com/watch?v=qCWuisB3_88">stampa del
    bilancio secondo codice</a>.</p>

</li>
</ul>
