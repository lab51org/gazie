<p>Men&ugrave; <strong>Contabilit&agrave;</strong>. Qui si raccolgono le
funzioni relative alla gestine della contabilit&agrave; generale,
incluse le stampe periodiche. Va osservato per&ograve; che i clienti e i
fornitori sono accessibili da altri men&ugrave;. Inoltre, la stampa dei
bilanci e le operazioni di chiusura e riapertura dei conti si trovano
invece nel men&ugrave; <strong>Fine anno</strong>.</p>

<p>Per accedere agli elenchi delle scritture, dei conti e delle causali
gi&agrave; esistenti, basta selezionare la voce principale relativa:
<strong>Prima nota</strong>, <strong>Piano dei conti</strong>,
<strong>Causali contabili</strong>, tralasciando le voci di men&ugrave;
ulteriori che appaiono mentre il puntatore del mouse vi si sposta
sopra.</p>

<ul>
<li>

    <p><strong>Prima nota</strong></p>

    <p>Da qui &egrave; possibile gestire le scritture contabili della
    contabilit&agrave; generale. Per inserire un nuovo movimento si deve
    scegliere la voce <strong>Nuovo movimento contabile</strong>, mentre
    per accedere alle scritture esistenti, si deve selezionare la voce
    principale di questo men&ugrave;.</p>

    <p>Esempi: <a title="guarda il video"
    href="http://www.youtube.com/watch?v=_ursC5We6nY">registrazione con
    causale libera</a>, <a title="guarda il video"
    href="http://www.youtube.com/watch?v=XYdFTPwQZFE">registrazione di
    un acquisto con una causale gi&agrave; predisposta</a>, <a
    title="guarda il video"
    href="http://www.youtube.com/watch?v=FKNz1l0gWLw">registrazione di
    un acquisto con una causale predisposta, ma da modificare</a>.</p>

</li>
<li>

    <p><strong>Piano dei conti</strong></p>

    <p>Da qui &egrave; possibile gestire il piano dei conti, stampare il
    partitario ed eventualmente stampare il piano dei conti evidenziando
    l'associazione alle voci di bilancio.</p>

    <p>Esempio: <a title="guarda il video"
    href="http://www.youtube.com/watch?v=IXyrXjLTO_Q">stampa del
    partitario di un cliente</a>.</p>

</li>
<li>

    <p><strong>Causali contabili</strong></p>

    <p>Da qui &egrave; possibile gestire le causali usate nelle
    scritture contabili. Le causali predefinite, riconoscibili
    perch&eacute; sono scritte con un testo tutto maiuscolo, non vanno
    modificate, essendo utilizzate automaticamente dalla procedura;
    tuttavia, si possono creare altre causali, in base alla propria
    convenienza.</p>

</li>
<li>

    <p><strong>Registri IVA</strong></p>

    <p>Da qui &egrave; possibile ottenere la stampa dei registri IVA, o
    «registri delle fatture».</p>

    <p>Esempi: <a title="guarda il video"
    href="http://www.youtube.com/watch?v=lNHfnjJx-tk">stampa del
    registro IVA degli acquisti in prova</a>, <a title="guarda il video"
    href="http://www.youtube.com/watch?v=Cxn2zZ3jlG8">stampa del
    registro IVA delle vendite in prova</a>.</p>

</li>
<li>

    <p><strong>Liquidazione IVA</strong></p>

    <p>Da qui &egrave; possibile ottenere la stampa della liquidazione IVA.</p>

</li>
<li>

    <p><strong>Libro giornale</strong></p>

    <p>Da qui &egrave; possibile ottenere la stampa del giornale di
    contabilit&agrave; generale.</p>

    <p>Esempio: <a title="guarda il video"
    href="http://www.youtube.com/watch?v=4q3PdZiBAbg">stampa del
    giornale di contabilit&agrave; generale in prova</a>.</p>

</li>
</ul>
